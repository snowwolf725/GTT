package gtt.macro.macroStructure;

public class MyColor {

	private int r = 0;
	private int g = 0;
	private int b = 0;
	
	MyColor(int _r, int _g, int _b) {
		r = _r;
		g = _g;
		b = _b;
	}
	
	public int getRed(){
		return r;
	}
	
	public int getGreen(){
		return g;
	}
	
	public int getBlue(){
		return b;
	}
	
}
